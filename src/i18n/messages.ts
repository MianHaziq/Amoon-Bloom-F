type Dict = (typeof messages)["en"];
type NestedKeys<T, P extends string = ""> = {
  [K in keyof T]: T[K] extends object
    ? NestedKeys<T[K], `${P}${P extends "" ? "" : "."}${K & string}`>
    : `${P}${P extends "" ? "" : "."}${K & string}`;
}[keyof T];

export type MessageKey = NestedKeys<Dict>;

/** Resolve a dot-path key for the given locale; falls back to English. */
export function t(
  locale: Locale,
  key: MessageKey,
  vars?: Record<string, string | number>
): string {
  const read = (dict: unknown): string | undefined => {
    let node: unknown = dict;
    for (const part of key.split(".")) {
      if (node && typeof node === "object" && part in node) {
        node = (node as Record<string, unknown>)[part];
      } else {
        return undefined;
      }
    }
    return typeof node === "string" ? node : undefined;
  };
  let str = read(messages[locale]) ?? read(messages.en) ?? key;
  if (vars) {
    for (const [k, v] of Object.entries(vars)) {
      str = str.replace(new RegExp(`\\{${k}\\}`, "g"), String(v));
    }
  }
  return str;
}

/**
 * Count-aware label: `count(n, locale, "units.itemOne", "units.itemOther")`
 * → "1 item" / "3 items" / "٣ منتجات". English distinguishes singular/plural;
 * Arabic uses a single readable form. Numbers stay Western (see `intlLocale`).
 */
export function tCount(
  locale: Locale,
  n: number,
  oneKey: MessageKey,
  otherKey: MessageKey
): string {
  return `${n} ${t(locale, n === 1 ? oneKey : otherKey)}`;
}
